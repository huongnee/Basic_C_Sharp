C# - Reflection
🔍 Reflection là gì?
Reflection trong C# là một cơ chế cho phép chương trình khám phá và tương tác với metadata (siêu dữ liệu) của các đối tượng, kiểu dữ liệu (class, method, property, v.v.) ngay trong lúc chương trình đang chạy (runtime).
👉 Nói đơn giản: Reflection cho phép bạn xem thông tin về một class, gọi các phương thức, đọc/ghi thuộc tính… mà không cần biết trước tên cụ thể trong mã nguồn.

🧠 Khi nào dùng Reflection?
Khi bạn muốn tạo các đối tượng, gọi phương thức hoặc truy cập thuộc tính mà không biết cụ thể tên class hoặc tên method lúc viết mã.
Khi làm việc với plugin, dependency injection, framework, test tự động, serialize/deserialize...
